// ### 2. **Assignment Operators** 
x = 5       // `=` (Simple assignment)   
console.log(x + " Simple Assignment")
x += 6      // `+=` (Add and assign)  
console.log(x + " Add and assign")
x -=2       // `-=` (Subtract and assign) 
console.log(x + " Subract and assign") 
x *= 3      // `*=` (Multiply and assign) 
console.log(x + " Multiply and assign") 
x /= 2      // `/=` (Divide and assign)  
console.log(x + " Divide and assign")
x %= 5      // `%=` (Modulus and assign) 
console.log(x + " Modulus and assign") 

