let Marks = 90;

if (Marks >= 90) {
    console.log('Grade A+');
}

else if (Marks >= 80 && Marks < 90) {
    console.log('Grade A');
}

else if (Marks >= 70 && Marks < 80) {
    console.log('Grade B');
}

else if (Marks >= 60 && Marks < 70) {
    console.log('Grade C');
}

else {
    console.log('fail');
}